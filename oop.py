# Class defines how objects of it's type are built and how they behave
# An object that is built from a class is also called an instance
# To build an object from a class, we only need to invoke the class ()

# class NameOfClass


class Patient:
    pass

print(Patient(), "Printed from Line 11") #invoking a class, creates an object/instance of that class

patient1 = Patient() #patient1 is now an instance of the Patient Class, and this process is called initilization or instanciation 

print(patient1)

